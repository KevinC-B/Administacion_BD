    private void tblEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmpleadosMouseClicked
        int seleccion = tblEmpleados.rowAtPoint(evt.getPoint());
        txtIdEmpleado.setText(tblEmpleados.getValueAt(seleccion, 0) + "");
        txtNombreEmpleado.setText(tblEmpleados.getValueAt(seleccion, 1) + "");
        txtCargoEmpleado.setText(tblEmpleados.getValueAt(seleccion, 2) + "");
        String fechaString = (String) tblEmpleados.getValueAt(seleccion, 3); // Cambia el índice si es necesario
        SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd"); // Ajusta el formato según sea necesario
        txtSalarioEmpleado.setText(tblEmpleados.getValueAt(seleccion, 4) + "");
        txtGerenteEmpleado.setText(tblEmpleados.getValueAt(seleccion, 5) + "");
        try {
            Date fechaRegistro = formato.parse(fechaString); //Convierte la cadena a Date
            dtFechaContratacionEmpleado.setDate(fechaRegistro); //Establece la fecha en el JDateChooser
        } catch (ParseException e) {
            e.printStackTrace(); //Maneja la excepción según sea necesario
            JOptionPane.showMessageDialog(null, "Error al convertir la fecha: " + e.getMessage());
        }
    }//GEN-LAST:event_tblEmpleadosMouseClicked

