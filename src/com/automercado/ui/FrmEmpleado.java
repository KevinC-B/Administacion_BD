    private void btnAgregarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarEmpleadoActionPerformed
        if (txtNombreEmpleado.getText().isEmpty() || txtCargoEmpleado.getText().isEmpty() || txtSalarioEmpleado.getText().isEmpty()
                || dtFechaContratacionEmpleado.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Por favor completo todos los campos");
        } else {
            Empleado emp = new Empleado();
            emp.setNombre_Empleado(txtNombreEmpleado.getText());
            emp.setCargo(txtCargoEmpleado.getText());
            Date fechaContratacion = dtFechaContratacionEmpleado.getDate();
            emp.setFecha_Contratacion(fechaContratacion);
            emp.setSalario(Integer.parseInt(txtSalarioEmpleado.getText()));
            emp.setId_Gerente(Integer.valueOf(txtGerenteEmpleado.getText()));

            EmpleadoBO empleadoBO = new EmpleadoBO();
            String mensaje = empleadoBO.agregarEmpleado(emp);

            JOptionPane.showMessageDialog(null, mensaje);
            limpiar();
            listarEmpleado();
            cargarTablaEmpleados();
        }
    }//GEN-LAST:event_btnAgregarEmpleadoActionPerformed

